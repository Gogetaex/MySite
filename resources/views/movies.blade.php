      <h2>Wolf Children (2012)</h2>
       <p>
        Mamoru Hasoda is a Japanese director who's directed several films, and this one is, in my opinion, by far his best. I don't even remember what made me put it on, but it hit me emotionally in the first few minutes harder than many movies hit me over their entire runtime, much like Up did.
        I don't want to say too much about the movie because it'd be very spoilery, but this is a great series for anyone who enjoys a slow paced character study that has no 'antagonist' other than the day to day getting through life, which is something we all face.
      </p>
    </div>
