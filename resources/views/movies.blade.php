      <h2>Ferris Buller's Day Off (1986)</h2>
       <p>
        This is a movie that didn't really teach me anything, but it's an absolute fun time that is so infectious, anyone can get caught up in it. Saw it for the first time with my old man around the same time I saw The Breakfast Club. It didn't exactly leave a mark in any sort of
        meaningful way, but it's definitely one of my favorite movies of all time. 
      </p>
