      <h2>Wreck-It Ralph (2012)</h2>
       <p>
        You never really go into a children's movie expecting it to have a profound impact on you. Most people from my generation were already sold on Wreck-It Ralph simply because of what it was about, and the cameos we knew we'd be seeing.
        I went into it excited to see Disney make a movie that would hopefully be as good as a Pixar film, and I left tearing up over a beautiful credits song by Owl City, a life lesson about loving myself, and a legitimately great movie that 
        taught me lessons I still use today.
      </p>
