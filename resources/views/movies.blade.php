      <h2>The Breakfast Club (1985)</h2>
       <p>
       One of the absolute best movies of all time. This means so much to me because it helped me get inside the heads of those around me. I saw it when I was sixteen and in the middle of my high school career, and the change it made was like night and day.
       I absolutely believe that every teenager should be required by law to watch this movie before going into high school. It shows that we're all going through the same struggles, and helps create empathy within the people watching. Each character felt so real to me and over the course of the movie,
       I felt like I'd made five new friends. A lot of who I am comes from having seen this movie.
      </p>
    </div>
