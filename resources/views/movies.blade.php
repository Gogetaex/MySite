      <h2>Pixar (1995-)</h2>
       <p>
        So Pixar is up there as one of the greatest studios ever made, and I personally think they're probably the most consistent studio to put out films. It's at the point with them that if I find out Pixar's name is on something, that's good enough for me to go ahead and claim 
        it as a masterpiece without ever having seen it. Every single movie that Pixar has put out has just been top notch, with Coco really blowing me away recently. Their understanding of, well, cinema outrivals all their competition and then some. Their movies don't always teach me a 
        life lesson, but they do always leave me feeling like a more fulfilled individual. 
      </p>
