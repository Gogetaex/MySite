        <p>
          In 2008, a sequel to Melee came out, and it was called Super Smash Bros. Brawl. This game was intentionally nutered so that it wouldn't be able to have the same insane skill ceiling that Melee had, and as such, it didn't prosper as a competitive game like Melee. 
          In 2011, a group of people got together to start modding Brawl to play like Melee. The result was Project M. For several years, they worked on perfecting it so that it'd be the best competitive Smash game, but at the end of 2015, they suddenly stopped developing it.
          Many theories came up, talks of C&D's from Nintendo, people claiming they wanted to monetize it, etc etc, but nothing concrete was ever found. The community decided to accept it, and settled on the current build, 3.6, as the final build for tournaments to keep long-term.
        </p>
