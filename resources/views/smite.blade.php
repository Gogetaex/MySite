      <h2>Scylla</h2>
      <p>
        So I mained midlane, which is predominantly a role dominated by mages. Of those mages, Scylla was my favorite god by far. She came out shortly after I started taking the game more seriously, and people started taking notice.
        Once I joined a real team, we started practicing against other teams, and part of each game is banning gods that aren't allowed to be picked, and it was quite a common occurrance for people to ban Scylla, not because she was
        too particularly good, but because it just wasn't worth letting me have her. Still definitely one of my favorite characters to play in any game ever. 
      </p>
