      <h2>Mercury</h2>
      <p>
        So Mercury was my primary God for 1v1's, and I actually barely played him outside of that. He had a very strong ability to play 1v1, and Scylla was one of the worst characters in the mode, so it was only natural to play someone else. What's comincal, though,
        is that he's based on going very fast, and even has references to Sonic, who is my main in Smash games. He's a late game god who if you don't stop him early, is going to run everyone over. That's part of why I liked him. He was hard to play in the start, 
        but by being smart, everything would usually end up going my way.
      </p>
