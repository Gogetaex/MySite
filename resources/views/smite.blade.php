        Much like in Conquest, bans were also available for 1v1. If Mercury got banned, I had to have another character that I was able to play, and that was He Bo. He was basically a burst mage who was able to unload all of his abilities very quickly, and is often
        called a glass cannon. He has no way of really escaping from bad situations other than "kill the thing threatening me before it kills me." He was a bit higher in the 'risk vs reward' department than I tend to enjoy playing, which is why he was more my secondary
