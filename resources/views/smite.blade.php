      <p class="smite2">
          Smite is from the genre most people call "MOBA" which stands for Multiple Online Battle Arena, otherwise known as the place hopes and dreams go to die. It's basically a five versus five team game where each player gets to pick a character (or in Smite, gods) 
          that has (usually) four different moves, with the objective of taking over the enemy's base. Other popular MOBAs would be League of Legends and DOTA 2. 
        </p>
        <p>
          I found the game early 2014 when a friend who was staying with me showed it to me, and I jumped in along with other friends absolutely full force, and it wasn't too long before we were able to start playing ranked. I was actually terrible when I started, 
          as I tend to be. After finally becoming less hard-headed about how I was doing things, and listened to other people giving me advice, I started to get better and climbed the ranks. There's also a 1v1 mode that I fell in love with because it meant I didn't
          have to count on anyone else and any mistake that was made was my own. I actually got to top ten in the world MMR wise at one point in this mode.
        </p>
        <p>
          I eventually got to a very high rank in the main mode, Conquest, as well. This is around the time where I was looking for a semi-professional team to join, and when I finally found one, it was an amazing feeling, because every second on the game felt like
          practice towards something greater. I was incredibly excited. Some time and a few losses later, I decided to put the game down, because my family and friends had told me that it was becoming 'too much.' Even so, it's a great game and I'm happy with the time 
          I put into it.
        </p>
