    <p style="padding-left:10%; padding-right:10%">
      So movies have been an instrumental part of my development, and have helped shape a lot of my beliefs. Often times, they really are just there to pass time and be entertaining, but occasionally, someone makes a movie that actually has something to say. These are the ones I really, really recommend.
    </p> 
  </div>  
  <hr>
  <div class="container">
    <div class="col-md-4">
      <img src="img/media/Futurama.jpg" alt="" class="img-responsive" />
    </div>
    <div class="col-md-8">
      <h2>Futurama</h2>
       <p>
        Someone recently asked me what I thought the smartest show on television was, and I realized that they were looking for the answer "Rick and Morty" when in reality, Futurama is the clear winner. I've been watching it since I was probably like, twelve, and as I've gotten older, 
        I keep catching more and more jokes and references in the reruns I've already seen ten times over. The writers are brilliant, even going so far as to create their own math forumla to get out of a problem they wrote themselves into, that they then incorporate into the show itself!
        The meta humor it has is also top notch, with a very colorful cast of characters to follow through their journies in the universe. The best part is, it's an episodic series with probably one of the greatest television endings of all time (even though it ended three times.)  
      </p>
    </div>
  </div>
  <hr>

    <div class="container">
    <div class="col-md-4">
      <img src="img/media/bojack.jpg" alt="" class="img-responsive" />
    </div>
    <div class="col-md-8">
      <h2>Bojack Horseman</h2>
       <p>
        Bojack Horseman is one of the few series that takes a very real look at depression, and other horrible things with a serious, yet silly attitude that most other series aren't willing to do. The main character isn't a good guy, his life isn't together, and it helps you see how far down the
        rabbit hole an individual can go when they're spiraling out of control. It shows you how hard it can be for someone who genuinely wants better for themselves to actually succeed, and the obstacles they face in order to make it there. It's an exceptional character study that helped me gain some perspective when I really needed it.w
      </p>
    </div>
  </div>
  <hr>

  <div class="container">
    <div class="col-md-4">
      <img src="img/media/adventure.jpg" alt="" class="img-responsive" />
    </div>
    <div class="col-md-8">
      <h2>Adventure Time</h2>
       <p>
        Adventure Time is one of those series that's primarily aimed at kids, but has such great writing, adults can enjoy it too. At least, that's where it starts out. The main character, Finn, grows and matures as the series goes on, along with the themes and elements present in the show, 
        by the time you get towards the later seasons, he's wrestling with real relationship and life issues, rather than just dealing with the wacky antics you see him go through towards the start of the series, and the colorful cast definitely grows on you as you get more invested in the show. 
        
      </p>
    </div>
  </div>
  <hr>
  
  <div class="container">
    <div class="col-md-4">
      <img src="img/media/dexter.jpg" alt="" class="img-responsive" />
    </div>
    <div class="col-md-8">
      <h2>Dexter</h2>
       <p>
        Dexter is actually one of the only live action television series that I enjoy, and I think it's because it actually has characters I find likeable, where most other series don't quite have that. Even the main character, a murderer, is still more interesting and likeable than many characters that are supposed
        to be likeable in other series. It does a very good job of showing both sides of the coin that is Dexter, which I felt was incredibly entertaining throughout the run of the series, and seeing just how far things can go with his world the way it was. 
      </p>
    </div>
  </div>
  <hr>

 <div class="container">
    <div class="col-md-4">
      <img src="img/media/gfalls.png" alt="" class="img-responsive" />
    </div>
    <div class="col-md-8">
      <h2>Gravity Falls</h2>
       <p>
        Gravity Falls is probably the best series to come out of Disney in a long while, and I think it's because it knew exactly what it wanted to do and didn't overstay its welcome. It had a couple big twists that were surprising for even the adults, and tons of clues aluding to the direction the story would go, 
        so people in the community had months and months to try and piece the mysteries together at the same time that the cast was attempting to do the same thing, which of course makes the experience that much more enjoyable.
      </p>
    </div>
  </div>
  <hr>

    <div class="container">
    <div class="col-md-4">
      <img src="img/media/bmirror.jpg" alt="" class="img-responsive" />
    </div>
    <div class="col-md-8">
      <h2>Black Mirror</h2>
       <p>
        Black Mirror is another one of those shows that really just makes you feel some kind of way. It's a story about the horrors of how technology could really mess up our lives, and it's absolutely fascinating. Anyone who enjoys asking the what ifs of, well, anything, and has the stomach for how deep the rabbit hole goes
        would definitely like this series. I found it at a time in my life where I really needed to think because I didn't have enough stimulation in my life, and boy oh boy did this give it to me. 
      </p>
    </div>
