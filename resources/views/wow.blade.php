        <p>
          So World of Warcraft was the first game that I actually played at a serious level, and I ended up getting really really good in the end, actually. 2008 to 2012 was definitely a time definitely dominated by WoW. It was something I took super casually in the beginning and absolutely loved, and it became something that made me feel like I had a second family,
          and it even became my passion when I realized that it was something I could play at a top level. Definitely one of the more impactful experiences on my life.
        </p>
        <p>
          I got into the game when my first girlfriend convinced me to convince my family to buy it for me the day after Christmas, and it was the primary thing we did together for a while, but he ended up getting bored and quit a lot, so I could't consistently play a character with her. After she quit, I realized that I still really enjoyed 
          the game, I kept playing, although I sucked pretty bad for quite a while. It wasn't until the expansion after I started playing, Wrath of the Lich King came out, that I actually started getting good and learning the ins and outs of the competitive side of the game, and ended up joining a fairly competitive guild and accomplishing some pretty 
          impressive stuff. The next expansion was when I really kicked it into high gear and started breaking into the top level. Ended up finding a long term home in a guild called Judgement with a lot of really cool guildies and a great leader who I'd ride into the fires of hell with. 
        </p>
        <p>
          Eventually, that guild broke apart and I ended up dipping out of the game. By the time I'd finished, I'd put a lot more time into it than I had realized and it motivated me to go and try out other games and see what the rest of the genres had to offer me in terms of competitive genres. Without WoW, I wouldn't have gotten a lot of the life's experience I needed to 
          handle certain situations I found myself in at different points of my life. It helped me learn how to talk to people, and it helped teach me a lot about myself.  
        </p>
